road = """Άσφαλτος σε Ντο Μινόρε

Ο δρόμος ανοίγει σαν βινύλιο παλιό,
σκάει και τρίζει στη βελόνα της βροχής.
Κάπου παίζει ένα μπλουζ — *the long and winding road*,
κι εγώ μετράω φανάρια σαν ρεφρέν.
Σοβαρός περπατώ, μα κλείνω το μάτι
στο πεζοδρόμιο που μου σφυρίζει ρυθμό.

Φανάρι με Ρίμα

Κόκκινο. Πράσινο. Κίτρινο.
Τρεις νότες για μια πόλη που ανασαίνει.
Ένα ραδιόφωνο από ταξί λέει *στα λιμάνια* ιστορίες,
κι ο δρόμος κάνει χορωδία.
Σοβαρός ο νόμος, παιχνιδιάρικη η καρδιά:
περνάω απέναντι με παύση.

Σκονισμένο Ρεφρέν

Στον δρόμο γράφτηκαν στίχοι με κιμωλία,
τους έσβησε ο χρόνος σαν κακός ντράμερ.
*Wish you were here* ψιθυρίζει μια αφίσα,
κι εγώ κρατάω το κουπλέ.
Η άσφαλτος θυμάται καλύτερα από εμάς.

Παράδρομος

Υπάρχει πάντα ένας παράδρομος
για όσους δεν χωρούν στη λεωφόρο.
Εκεί παίζει ένα παλιό λαϊκό,
σπασμένο, τίμιο.
Σοβαρά πράγματα συμβαίνουν χαμηλά,
με παιδικά παπούτσια.
Πεζοδρόμιο με Κιθάρα

Έδεσα τα κορδόνια στον ρυθμό,
η πόλη κουρδίζεται.
Κάποιος τραγουδά *like a rolling stone*,
κι ο δρόμος γελάει χωρίς λόγια.
Η σοβαρότητα είναι στάση σώματος,
όχι ψυχής.

Νυχτερινή Διασταύρωση

Δύο δρόμοι διαφωνούν στο σκοτάδι,
ποιος θα πάει πρώτος.
Ένα μπάσο από μπαρ κάνει τον διαιτητή,
*another brick* πέφτει στη σιωπή.
Περνάω ανάμεσα, με σεβασμό και πονηριά.

Χάρτης Τσέπης

Ο δρόμος διπλώνεται στην τσέπη,
μα ξεδιπλώνεται στα βήματα.
Ένα τραγούδι από τα παλιά
κρατάει κατεύθυνση.
Σοβαρός ο προορισμός,
παιχνίδι η διαδρομή.

Γκράφιτι σε Μέτρο

Οι τοίχοι γράφουν πριν από εμάς.
Ο δρόμος διαβάζει δυνατά.
*Heroes* για τρία λεπτά,
και μετά σιωπή.
Η σοβαρότητα βάφεται σπρέι,
και χαμογελά.

Αλάτι και Βροχή

Η βροχή αλατίζει την άσφαλτο,
σαν παλιό 45άρι.
Ένα ρεφρέν κολλάει στο παπούτσι,
*κάποτε θα γυρίσω*.
Ο δρόμος δεν υπόσχεται,
μα κρατάει ρυθμό.
Τελικό Φανάρι

Στο τέλος ο δρόμος δεν τελειώνει,
απλώς αλλάζει τόνο.
Παίζει χαμηλά ένα τραγούδι αποχαιρετισμού,
και γελάει με σοβαρότητα.
Περνάω το φανάρι,
κρατώντας το παιχνίδι ζωντανό.

---

Δρόμος σε Επανάληψη

Ο δρόμος παίζει τον εαυτό του ξανά,
σαν τραγούδι που δεν βαριέσαι.
Ένα ρεφρέν κολλάει στον αέρα — *on the road again*,
κι εγώ το σιγοπατάω.
Σοβαρός ο χρόνος,
παιχνιδιάρικη η επιστροφή.

Στάση Χωρίς Όνομα

Η στάση δεν έχει πινακίδα,
μόνο ανθρώπους.
Κάποιος σιγομουρμουρίζει ένα παλιό ροκ,
κι ο δρόμος κρατάει τον τόνο.
Σοβαρό να περιμένεις,
παιχνίδι να ελπίζεις.

Λεωφόρος με Αντίλαλο

Η λεωφόρος μιλάει δυνατά,
μα ακούει τα πάντα.
*Everybody hurts* περνάει σαν ψίθυρος,
ανάμεσα σε κόρνες.
Ο δρόμος ξέρει:
η σοβαρότητα έχει καρδιά.

Σχισμή στην Άσφαλτο

Μια ρωγμή ανοίγει διάλογο,
ανάμεσα σε δύο βήματα.
Ένα τραγούδι μπλέκεται — *comfortably numb*,
κι ο δρόμος χαμογελά.
Σοβαρά πράγματα χωράνε
σε μικρές σχισμές.

Παπούτσια σε Ρυθμό

Τα παπούτσια γράφουν παρτιτούρα,
πάνω στο πεζοδρόμιο.
Ένα ντραμς από μακριά λέει *come together*.
Ο δρόμος συγχρονίζεται.
Σοβαρός ο ρυθμός,
παιχνίδι το περπάτημα.

Πόλη σε Χαμηλή Ένταση

Η πόλη κατεβάζει την ένταση,
όχι το νόημα.
Ένα τραγούδι περνάει — *under pressure*,
χωρίς να φωνάζει.
Ο δρόμος αντέχει,
κι αστειεύεται διακριτικά.

Στροφή

Στη στροφή δεν φαίνεται το τέλος,
μόνο η πρόθεση.
Κάπου παίζει ένα παλιό ελληνικό,
χωρίς τίτλο.
Ο δρόμος σοβαρεύει,
κι αμέσως μετά κλείνει το μάτι.

Κάτω από τις Πινακίδες

Οι πινακίδες δείχνουν κατεύθυνση,
όχι λόγο.
*Road to nowhere* περνάει σαν αστείο.
Ο δρόμος γελάει πρώτος.
Η σοβαρότητα δεν χάνεται,
απλώς χαλαρώνει.

Μικρή Ευθεία

Μια μικρή ευθεία,
αρκετή για σκέψη.
Ένα τραγούδι κρατάει ίσα — *stay*,
κι ο δρόμος συμφωνεί.
Σοβαρός δεν σημαίνει ακίνητος.

Δρόμος Μετά τα Μεσάνυχτα

Μετά τα μεσάνυχτα ο δρόμος μιλάει χαμηλά,
σαν μυστικό.
Ένα τελευταίο τραγούδι παίζει — *goodnight*.
Η άσφαλτος αναπνέει.
Σοβαρό το σκοτάδι,
παιχνιδιάρικο το φως.

---

Ιπποκράτους (Με Παραμόρφωση)

Η Ιπποκράτους βουίζει σαν ενισχυτής,
καλώδια, καφές και νεύρο.
Μια κιθάρα ουρλιάζει *paranoid*,
ανάμεσα σε βιβλιοπωλεία.
Ο δρόμος δεν θεραπεύει,
μα λέει την αλήθεια δυνατά.

Πανεπιστημίου — Τρία Ριφ

Τρεις δρόμοι τέμνονται στο μυαλό:
παρελθόν, παρόν, αντίσταση.
Η Πανεπιστημίου παίζει σε τρία τέταρτα,
*another one bites the dust*.
Σοβαρή λεωφόρος,
με υπόγειο headbang.

Νέα Φιλαδέλφεια (Βόρεια Ένταση)

Στη Νέα Φιλαδέλφεια ο αέρας είναι βαρύς,
σαν power chord.
Γειτονιές χτυπάνε ρυθμό,
*seek and destroy* στα χαμηλά.
Ο δρόμος κρατάει μνήμη,
και δεν χαμηλώνει ποτέ.

Τρώων (Στενός αλλά Ηλεκτρικός)

Η Τρώων δεν φωνάζει,
τρίζει.
Μια μπάντα πρόβα σε υπόγειο,
*smells like teen spirit*.
Ο δρόμος ξέρει από πολιορκίες,
και από επιμονή.

Αλεξάνδρας — Σιδερένιος Παλμός

Η Αλεξάνδρας κυλάει βαριά,
σαν ντραμς πολέμου.
Λεωφορεία, νοσοκομεία, φωνές,
*war pigs* στον αέρα.
Σοβαρός δρόμος,
χωρίς ανάγκη για συγγνώμη.

Μάχη Αναλάτου (Χωρίς Ρεφρέν)

Εδώ δεν χωράει ρεφρέν,
μόνο κουπλέ.
Η άσφαλτος θυμάται ονόματα,
σαν doom riff.
Ο δρόμος στέκεται όρθιος,
με ενισχυτή την ιστορία.

Κύπρου (Με Σκιά και Feedback)

Η Κύπρου περνάει χαμηλά,
μα αφήνει feedback.
Μπαλκόνια, σημαίες, σιωπές,
*no more tears*.
Ο δρόμος δεν ξεχνά,
απλώς παίζει πιο αργά.

Τρεις Δρόμοι — Μία Συγχορδία

Τρεις δρόμοι μπλέκονται,
σαν δάχτυλα σε ταστιέρα.
Δεν υπάρχει σωστός,
μόνο ένταση.
Ο δρόμος διαλέγει distortion,
και προχωρά.

Νυχτερινή Αλεξάνδρας (Metal Ballad)

Τη νύχτα η Αλεξάνδρας μαλακώνει,
χωρίς να υποχωρεί.
Ένα σόλο σκίζει τη σιωπή,
*fade to black*.
Σοβαρό το βάρος,
ηλεκτρική η λύτρωση.

Τελικός Δρόμος (Encore)

Ο δρόμος ζητά encore,
όχι χειροκρότημα.
Ιδρώτας, άσφαλτος, ενισχυτές,
*rock you like a hurricane*.
Σοβαρά ζούμε,
δυνατά περπατάμε.
"""
print(road)
